    void openThongKe() {
        if (Auth.isLogin()) {
            if (!Auth.isManager()) {
                MsgBox.showMessageDialog(this, "Bạn không có quyền xem thông tin doanh thu");
                return;
            }
            new ThongKe_Dialog(this, true).setVisible(true);
        } else {
            MsgBox.showMessageDialog(this, "Vui lòng đăng nhập");
        }
    }

    private void processLoginSuccessful() {
        try {
            user.setText(Auth.user.getMaNV());
            if (Auth.user.isVaiTro() == true) {
                vaitro.setText("Quản lí");
            } else {
                vaitro.setText("Nhân viên");
            }
        } catch (Exception e) {
            MsgBox.showMessageDialog(this, "Vui lòng đăng nhập để sử dụng hệ thống !");
            this.dispose();
        }
    }

    void dangxuat() {
        Auth.clear();
        new Login_Dialog(this, true).setVisible(true);
        processLoginSuccessful();
        this.dispose();
    }

